#include <iostream>
#include <cstdio>
#include <cstring>

using namespace std;
int k;
int matrix[800][800];

int main(){
    while(~scanf("%d",&k)){
        for(int i=0;i<k;++i){
            for(int j=0;j<k;++j){
                scanf("%d",&matrix[i][j]);
            }
        }

    }
    return 0;
}